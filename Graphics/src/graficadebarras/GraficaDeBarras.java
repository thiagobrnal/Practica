    public int MayorValor(int valRojo, int valVerde, int valAzul){
        
        if (valRojo > valVerde && valRojo > valAzul) {
            return valRojo;
            
        }else if(valVerde > valAzul){
            return valVerde;
            
        }else{
            return valAzul;
            
        }
    }
    
    public void paint(Graphics g){
        super.paint(g);
        
        if (bandera == true) {
            //guardo los textField en String
            String sRojo = txt_rojo.getText();
            String sVerde = txt_verde.getText();
            String sAzul = txt_azul.getText();
            
            //paso los String a Enteros
            int intRojo = Integer.parseInt(sRojo);
            int intVerde = Integer.parseInt(sVerde);
            int intAzul = Integer.parseInt(sAzul);
            
            int valMayor = MayorValor(intRojo, intVerde, intAzul);
            
            //multiplico por 400 por el maximo tamaño deseado
            int largoRojo = intRojo * 400 / valMayor;
            int largoVerde = intVerde * 400 / valMayor;
            int largoAzul = intAzul * 400 / valMayor;
            
            g.setColor(new Color(255,0,0));
            g.fillRect(100, 100, largoRojo, 40);
            g.drawString("Color Rojo", 15, 125);
            
            g.setColor(new Color(0,130,0));
            g.fillRect(100, 150, largoVerde, 40);
            g.drawString("Color Verde", 15, 175);
            
            g.setColor(new Color(0,0,255));
            g.fillRect(100, 200, largoAzul, 40);
            g.drawString("Color Azul", 15, 225);
           
        } 
    }
    
